_BASE_TEXTS = _STATIC_TRANSLATIONS['en']
_translation_cache: dict = {}


def _translate_batch_sync(texts: list, target_language: str) -> list:
    """Synchronous Google Translate call used only for the /translations endpoint."""
    import requests
    cache_key = f"{target_language}_{hash(tuple(texts))}"
    if cache_key in _translation_cache:
        return _translation_cache[cache_key]
    try:
        combined = "\n".join(texts)
        r = requests.get(
            "https://translate.googleapis.com/translate_a/single",
            params={"client": "gtx", "sl": "en", "tl": target_language, "dt": "t", "q": combined},
            timeout=4,
        )
        if r.status_code == 200:
            translated = "".join(part[0] for part in r.json()[0] if part[0])
            result = translated.split("\n")
            _translation_cache[cache_key] = result
            return result
    except Exception:
        pass
    return texts


def get_translations(language_code: str) -> dict:
    if language_code in _STATIC_TRANSLATIONS:
        return _STATIC_TRANSLATIONS[language_code]

    cache_key = f"full_{language_code}"
    if cache_key in _translation_cache:
        return _translation_cache[cache_key]

    keys = list(_BASE_TEXTS.keys())
    translated = _translate_batch_sync(list(_BASE_TEXTS.values()), language_code)
    result = dict(zip(keys, translated))
    _translation_cache[cache_key] = result
    return result
